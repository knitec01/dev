package LJS.chapter01.ex02;

public class CharEx {
    public static void main(String[] args) {
        // char형 변수 character1를 문자 'A'로 초기화
        char character1 = 'A';

        // char형 변수 character2를 정수(리터럴)65로 초기화
        char character2 = 65;

        // ''빈 문자입력시 컴파일 오류
        // char character3 = '';

        System.out.println(character1); // A
        System.out.println(character2); // A
    }
}
